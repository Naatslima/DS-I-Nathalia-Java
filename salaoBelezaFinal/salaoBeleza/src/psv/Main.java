    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        Connection con = Conexao.abrirConexao();
        ProcedimentoBean pb = new ProcedimentoBean();
        ProcedimentoDAO pd = new ProcedimentoDAO(con);
        pb.setCod_cliente(Integer.parseInt(txtCodCliente.getText()));
        Object[] opcoes = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null, "Deseja excluir cliente " + txtNome.getText() + "?", "Exclusão",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]
        );

        if (i == JOptionPane.YES_OPTION) {
            labelMensagem.setText(pd.excluir(pb));
            btnPesquisarMouseClicked(evt);
        }
        Conexao.fecharConexao(con);
    }//GEN-LAST:event_btnExcluirMouseClicked

