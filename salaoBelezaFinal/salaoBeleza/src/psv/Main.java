    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked

        Connection con = Conexao.abrirConexao();
        ProcedimentoDAO pd = new ProcedimentoDAO(con);
        
        List<ProcedimentoBean> listaProcedimento = new ArrayList<>();
        listaProcedimento = pd.listarTodos();
        
        DefaultTableModel tbm
                = (DefaultTableModel) tblConsulta.getModel();
        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;
        for (ProcedimentoBean pb : listaProcedimento) {
            tbm.addRow(new String[1]);
            System.out.println(pb.getNome());
            tblConsulta.setValueAt(pb.getNome(), i, 0);
            tblConsulta.setValueAt(pb.getTelefone(), i, 1);
            tblConsulta.setValueAt(pb.getLimpezaDePele(), i, 2);
            tblConsulta.setValueAt(pb.getUnhaGel(), i, 3);
            tblConsulta.setValueAt(pb.getUnhaFibra(), i, 4);
            tblConsulta.setValueAt(pb.getPodologia(), i, 5);
            tblConsulta.setValueAt(pb.getEscovaCabelo(), i, 6);
            tblConsulta.setValueAt(pb.getPinturaCabelo(), i, 7);
            tblConsulta.setValueAt(pb.getLuzesCabelo(), i, 8);
            tblConsulta.setValueAt(pb.getCorteCabelo(), i, 9);
            tblConsulta.setValueAt(pb.getMassagem(), i, 10);
            tblConsulta.setValueAt(pb.getDrenagemLinfatica(), i, 11);
            tblConsulta.setValueAt(pb.getBanhoDeLama(), i, 12);
            tblConsulta.setValueAt(pb.getCod_cliente(), i, 13);
           
            i++;
        }

        

        Conexao.fecharConexao(con);
    }//GEN-LAST:event_btnPesquisarMouseClicked

